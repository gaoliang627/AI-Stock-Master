def is_english() -> bool:
    """
    判断当前语言是否为英文
    
    返回:
        bool: 如果当前语言是英文返回True，否则返回False
    """
    try:
        # 首先检查环境变量强制设置
        forced_lang = os.environ.get('FORCE_ENGLISH', '').lower()
        if forced_lang in ['1', 'true', 'yes']:
            return True
        
        # 使用get_current_language()函数判断
        current_lang = get_current_language()
        return current_lang == 'en_US'
    except Exception:
        return CURRENT_LANGUAGE == 'en_US'